    private void ValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderActionPerformed
        ok = true;
        this.lot.setDescription(Descriptif.getText());
        if(BBon.isSelected()&&combobox.getSelectedIndex()==0)
        {
              this.lot.setDescription(Descriptif.getText()+" d'une valeur de 30‚Ç¨");  
        }
        if(BBon.isSelected()&&combobox.getSelectedIndex()==1)
        {
            this.lot.setDescription(Descriptif.getText()+" d'une valeur de 50‚Ç¨");
        }
        if(BBon.isSelected()&&combobox.getSelectedIndex()==2)
        {
            this.lot.setDescription(Descriptif.getText()+" d'une valeur de 90‚Ç¨");
        }
        if(BCadeau.isSelected())
        {
            this.lot.setImage(pi.getImage());
        }
        
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_ValiderActionPerformed

